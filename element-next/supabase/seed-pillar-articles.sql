  <li><strong>Leads qualificados por canal:</strong> Quantos pedidos de orçamento ou contactos geraste por canal?</li>
  <li><strong>ROI por canal:</strong> Qual o retorno de cada euro investido em cada canal?</li>
  <li><strong>Posição média no Google:</strong> Para keywords relevantes, estás a subir ou a descer?</li>
</ul>

