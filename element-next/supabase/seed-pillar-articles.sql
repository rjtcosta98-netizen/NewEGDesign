<h2>Perguntas Frequentes</h2>
<dl>
<dt>Quanto deve uma PME portuguesa gastar em marketing digital por mês?</dt>
<dd>Não existe um valor único, mas como referência: PMEs em fase de crescimento investem tipicamente entre 5% e 10% da faturação em marketing digital. Para começar, 300 € a 500 €/mês focados em SEO e Google Ads é um ponto de entrada realista que já produz resultados mensuráveis. O mais importante não é quanto gastas, mas a eficiência — mede o retorno de cada euro e realoca para os canais com melhor performance.</dd>

<dt>O que é mais eficaz: SEO ou Google Ads para PMEs?</dt>
<dd>São complementares, não concorrentes. Google Ads dá resultados imediatos mas pára quando paras de pagar. SEO é mais lento a arrancar mas cria um ativo permanente que gera tráfego durante anos. A estratégia ideal para PMEs é usar Google Ads para resultados a curto prazo enquanto o SEO ganha força, e depois reduzir gradualmente o investimento em Ads à medida que o orgânico cresce.</dd>

<dt>As redes sociais são suficientes para substituir um website?</dt>
<dd>Não. As redes sociais são jardins fechados — o teu conteúdo não aparece no Google, podes perder a conta a qualquer momento e não consegues personalizar completamente a experiência de compra. O website é a tua base digital permanente; as redes sociais são o megafone que direciona tráfego para essa base. Para resultados sérios, precisas obrigatoriamente de ambos.</dd>

<dt>Em quanto tempo posso esperar resultados do marketing digital?</dt>
<dd>Depende do canal. Google Ads produz resultados nos primeiros dias. Redes sociais com publicidade paga em 1 a 2 semanas. SEO orgânico em 3 a 6 meses para keywords de média concorrência, 6 a 12 meses para keywords competitivas. Email marketing tem retornos crescentes com o tamanho da lista. O marketing digital é um sistema que se acumula — os resultados crescem de forma composta com o tempo.</dd>

<dt>Preciso de uma agência de marketing digital ou posso fazer tudo internamente?</dt>
<dd>Depende dos teus recursos. Para PMEs sem equipa de marketing dedicada, uma agência garante consistência, ferramentas profissionais e conhecimento técnico atualizado — especialmente em SEO técnico e gestão de campanhas pagas. Para redes sociais e newsletter, uma pessoa interna com formação básica pode ser suficiente. A combinação de agência para SEO e paid media mais um recurso interno para conteúdo e redes sociais é frequentemente a solução mais eficaz por euro investido.</dd>

<dt>Como sei se o meu marketing digital está a funcionar?</dt>
<dd>Com métricas corretas configuradas desde o início. O mínimo indispensável: Google Analytics 4 com goals de conversão (pedidos de contacto, compras, chamadas), Google Search Console para monitorizar keywords e posições, e rastreio de conversões em Google Ads se tiveres campanhas ativas. Revê mensalmente o número de leads por canal, custo por lead e taxa de conversão do website. Estes dados dão-te a clareza necessária para alocar o orçamento de forma inteligente.</dd>
</dl>
